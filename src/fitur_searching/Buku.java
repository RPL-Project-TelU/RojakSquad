public class Buku {
    private String judul;
    private int rating;
    private String tipe;
    
    public String getJudul(){
        return this.judul;
    }
    public void setJudul(String judul){
        this.judul = judul;
    }
    public int getRating(){
        return this.rating;
    }
    public void setRating(int rating){
        this.rating = rating;
    }
    public String getTipe(){
        return this.tipe;
    }
    public void setTipe(){
        this.tipe = tipe;
    }
}
